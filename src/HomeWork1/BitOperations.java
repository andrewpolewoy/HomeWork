        //42 >>> 15     101010                           1111                              0
        //42 << 15      101010                           1111                              1376256
        //42 <<= 15     101010                           1111                              1376256
        //42 >>>= 15    101010                           1111                              0

        //Операция:     -42                               -15                               результат:

        //-42           11111111111111111111111111010110
        //-15           11111111111111111111111111110001
        //~ -42         11111111111111111111111111010110                                   41
        //~ -15         11111111111111111111111111110001                                   14
        //-42 & -15     11111111111111111111111111010110 11111111111111111111111111110001 -48
        //-42 &= -15    11111111111111111111111111010110 11111111111111111111111111110001 -48
        //-42 | -15     11111111111111111111111111010110 11111111111111111111111111110001 -9
